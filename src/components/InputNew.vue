<script setup>
import { ref} from "vue";
//const props = defineProps(["onNewItem"]);
const text = ref("");
const emits = defineEmits(["onNewItem"]);

function handleKeyUp(evt) {
  if(text.value === "" && evt.key !== "Enter"){
    return;// Si el campo de texto est√° vac√≠o y la tecla presionada no es "Enter", se sale de la funci√≥n sin realizar ninguna acci√≥n adicional.
  }

  if (evt.key === "Enter") {
    if(text.value !== ""){
      emits("onNewItem", text);
      text.value = "";
    }else{
      alert("It is necessary to enter the name of the task to be able to be added üò£")
    }
  }

}
</script>

<template><input v-model="text" @keyup="handleKeyUp" placeholder="Add new task" /></template>

